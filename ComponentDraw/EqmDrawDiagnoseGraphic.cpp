void CEqmDrawDiagnoseGraphic::DeleteItem( const CString itemName )
{
	vector<_tagDiagnoseItem>::iterator iter = m_vecDiagnoseItem.begin();
	while (iter != m_vecDiagnoseItem.end())
	{
		if ((*iter).strItemName == itemName)
		{
			DeleteAllSubItem((*iter));
			iter = m_vecDiagnoseItem.erase(iter);
			break;
		}
		else
		{
			++iter;
		}
	}
	m_bNeedSave = true;
}

void CEqmDrawDiagnoseGraphic::DeleteAll()
{
	vector<_tagDiagnoseItem>::iterator iter = m_vecDiagnoseItem.begin();
	while (iter != m_vecDiagnoseItem.end())
	{
		DeleteAllSubItem((*iter));
		iter = m_vecDiagnoseItem.erase(iter);
	}
	m_bNeedSave = true;
}

